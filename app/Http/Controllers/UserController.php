    public function changePassword(Request $request){
        try {
            $user = Auth::user();
            if(Hash::check($request->password_actual, $user->password)){
                $user->password = bcrypt($request->password);
                $user->update();
                return response()->json(['updated' => true, 'message' => 'Actualizado Correctamente'], 200);
            }else{
                return response()->json(['updated' => false, 'message' => 'Contrase√±a Invalida, Reintente'], 200);
            }
        } catch (\Throwable $th) {
            dd($th);
        }
    }

